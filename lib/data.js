/**
 * getAllProducts()
 * ----------------
 * מדמה שליפה של כל המוצרים ממסד נתונים.
 *
 * השורה "await new Promise(...)" יוצרת השהייה מדומה של 500ms.
 * זה מחקה קריאת API אמיתית שלוקחת זמן להגיב.
 *
 * באפליקציה אמיתית, זה היה נראה משהו כזה:
 *   const response = await fetch('https://api.example.com/products');
 *   const data = await response.json();
 *   return data;
 */
export async function getAllProducts() {
    // מדמה השהיית רשת (500 מילישניות)
    await new Promise((resolve) => setTimeout(resolve, 500));

    // מחזיר את כל המוצרים (כמו שמסד נתונים היה עושה)
    return products;
}

/**
 * getProductById(id)
 * -------------------
 * מדמה שליפה של מוצר בודד לפי המזהה שלו.
 *
 * הפונקציה .find() מחפשת במערך מוצר שהמזהה שלו
 * תואם למה שאנחנו מחפשים.
 *
 * מחזיר את המוצר אם נמצא, או null אם לא.
 */
export async function getProductById(id) {
    // מדמה השהיית רשת
    await new Promise((resolve) => setTimeout(resolve, 300));

    // מוצא את המוצר עם מזהה תואם
    // parseInt() ממיר את המחרוזת "1" למספר 1
    const product = products.find((p) => p.id === parseInt(id));

    // מחזיר את המוצר (או null אם לא נמצא)
    return product || null;
}
